    function submitForm() {
        // フォームのデータを取得
        var formData = $("#searchForm").serialize();

        // AJAXリクエストを作成
        $.ajax({
            type: "POST",
            url: "search.php",
            data: formData,
            success: function(response) {
                // 検索結果を表示
                $("#searchResults").html(response);
            },
            error: function(error) {
                console.log("エラーが発生しました:", error);
            }
        });
    }
